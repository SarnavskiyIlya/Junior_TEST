package Task_3;

/**
 * Интерфейс с абстракнтыми методами простых операций
 */
public interface Operations {

    /**
     * Метод для вычисления суммы переменных
     */
    void addition(int number);

    /**
     * Метод для вычисления разности переменных
     */
    void subtraction(int number);

    /**
     * Метод для вычисления произведения переменных
     */
    void multiplication(int number);

    /**
     * Метод для вычисления частного переменных
     */
    void division(int number);
}
